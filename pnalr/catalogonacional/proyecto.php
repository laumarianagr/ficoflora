						<br />						
						<p>
							Con base en lo anteriormente expuesto se puede evidenciar que poseemos una importante información florística y taxonómica de 
							muchas regiones del país, pero estos datos se encuentran en muchos casos desactualizados y dispersos. Transcurridas más tres 
							décadas no ha habido una actualización integral del "Catálogo Nacional de Algas Bénticas y pastos marinos de Venezuela" 
							(<i>A Catalog of Benthic Marine Algae and Seagrasses of Venezuela</i>), publicado por Ganesan en 1989. Por otra parte mucha de la 
							información publicada corresponde a listas florísticas, sin descripciones, claves, ilustraciones, ni mapas, lo cual hace de estas 
							listas, herramientas poco prácticas y con bajo atractivo didáctico. El presente catálogo contiene la recopilación bibliográfica 
							ficoflorística desde las referidas publicaciones del 1799 hasta el presente.
						</p>									
						<p>
							Nos enfocamos entonces en la creación del primer <span class="caligrafia">Catálogo Digital Taxonómico de las Macroalgas Bénticas Marinas 
							de Venezuela</span> a partir de la información taxonómica y geográfica actualizada, utilizando como principal herramienta la 
							sistematización y conformación de una base de datos de dominio público, que permitirá almacenar la información y presentarla a través 
							de este sitio web, diseñado para ser consultado desde el dispositivo de su preferencia. 
						</p>				
